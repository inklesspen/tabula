class EventType(int, enum.Enum):
    EV_SYN = (0, SynCode)
    EV_KEY = (1, KeyCode)
    EV_REL = (2, RelCode)
    EV_ABS = (3, AbsCode)
    EV_MSC = (4, MscCode)
    EV_LED = (17, LedCode)

    def __new__(cls, value, codes: EventCode):
        obj = int.__new__(cls, value)
        obj._value_ = value
        obj._codes = codes
        return obj

    def __getattr__(self, name):
        return getattr(self._codes, name)


class Event(msgspec.Struct, frozen=True, array_like=True, kw_only=True):
    type: EventType
    code: EventCode
    value: int | None
    timestamp: datetime.timedelta

    @classmethod
    def from_libevdev_event(cls, evt):
        _type = EventType(evt.type.value)
        return cls(
            type=_type,
            code=_type._codes(evt.code.value),
            value=evt.value,
            timestamp=datetime.timedelta(seconds=evt.sec, microseconds=evt.usec),
        )

    @classmethod
    def from_log(cls, type_name, code_name, value, seconds, microseconds):
        _type = EventType[type_name]
        _code = _type._codes[code_name]
        return cls(type=_type, code=_code, value=value, timestamp=datetime.timedelta(seconds=seconds, microseconds=microseconds))

    def to_log(self):
        return f"Event.from_log({self.type.name!r}, {self.code.name!r}, value={self.value!r}, seconds={self.timestamp.seconds}, microseconds={self.timestamp.microseconds})"


